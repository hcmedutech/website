                <h5>Children are vulnerable by nature</h5>
                <p>
                    As an institution that fosters growth, we understand that child development is complex.
                    Students have not reached their full cognitive capability during their early years.
                    In designing interfaces and physical structures, it is important to consider that they should be immersed in safe environments.
                    Staying away from sharp objects or anything that can physically harm them should be avoided at best.
                    To have a coherent experience across the institution, a design language was made to create a consistent look and feel to the brand and digital experiences.<br>
                    <br>
                    It is important to minimize sharp edges, all design elements are now rounded including shapes and even the typography.
                    Also, the color is kept consistent and easy on the eyes so that people from all ages can easily use and read what's on the screen or print.
